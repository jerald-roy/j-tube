const generateAccessAndRefereshTokens = async(userId) =>{
    try {
        const user = await User.findById(userId)
        const accessToken = user.generateAccessToken()
        const refreshToken = user.generateRefreshToken()

        //we are only storing th refresh token on the backend because access token are short lived and it gets cross checked or verified when asking for the resource by jwt.verify()
        user.refreshToken = refreshToken
        //the below validateBeforeSave is used because : to skip the validation step and just save the new refreshToken quickly.
        await user.save({ validateBeforeSave: false })

        return {accessToken, refreshToken}


    } catch (error) {
        throw new ApiError(500, "Something went wrong while generating referesh and access token")
    }
}

